public class IncrementDecrement {
    static void demoOperators() {
        int x = 5;
        System.out.println("Original value: " + x);

        x++;
        System.out.println("After increment: " + x);

        x--;
        System.out.println("After decrement: " + x);
    }

    public static void main(String[] args) {
        demoOperators();
    }
}
